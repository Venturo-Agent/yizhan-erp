/** 簡體字轉繁體（逐字對照） */
export function toTraditional(text: string | null | undefined): string {
  if (!text) return ''
  return text
    .split('')
    .map(char => SIMPLIFIED_TO_TRADITIONAL[char] || char)
    .join('')
}

/** 性別格式標準化：M/MALE/男 → 'M'，F/FEMALE/女 → 'F'，其餘 → null */
export function normalizeGender(gender: string | null | undefined): 'M' | 'F' | null {
  if (!gender) return null
  const g = gender.toUpperCase()
  if (g === 'M' || g === 'MALE' || gender === '男') return 'M'
  if (g === 'F' || g === 'FEMALE' || gender === '女') return 'F'
  return null
}
